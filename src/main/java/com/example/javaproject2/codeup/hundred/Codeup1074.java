package com.example.javaproject2.codeup.hundred;

import java.util.Scanner;

public class Codeup1074 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int num = sc.nextInt();
        for (int i = num; i > 0; i--) {
            System.out.println(i);
        }
    }
}