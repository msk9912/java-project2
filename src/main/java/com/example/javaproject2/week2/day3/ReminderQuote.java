package com.example.javaproject2.week2.day3;

public class ReminderQuote {
    public static void main(String[] args) {
        int iVal = 1;
        System.out.println(iVal / 2);

        // 몫
        int quote = 10 / 3;
        //나머지
        int remainder = 10 % 3;

        System.out.println(quote);
        System.out.println(remainder);
    }
}
